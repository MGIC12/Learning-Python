# range(x) 
# Range crea una secuencia de números que parte desde 0 hasta el x-1
# Si range tiene dos números (x, y) se representa como range(inicio, fin)
# Y si range tiene 3 números como parámetros (x, y, c) se representa como range(inicio, fin , de cuanto en cuanto)
print("f°      c°")
for tempf in range(100, 110, 9):
    print(tempf,"      ",int((tempf)*5/9))